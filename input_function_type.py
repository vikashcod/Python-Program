# input value convert integer but not input charater   
a=input("enter a number\t")
print(a)
print(type(a))
a=int(a)
print(type(a))
# input value convert floating but not input charater
b=input("enter b number\t")
print(b) 
print(type(b))
b=float(b)
print(type(b))
# input value convert  boolean but not input charater
c=input("enter c number\t")
print(c) 
print(type(c))
c=bool(c)
print(type(c))
# input value convert strings but not input charater
d=input("enter d number\t")
print(d) 
print(type(d))
d=str(d)
print(type(d))

 
